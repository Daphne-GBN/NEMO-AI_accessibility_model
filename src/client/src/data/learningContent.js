const getNumbersData = () => {
    const numbers = [];
    for (let i = 0; i <= 100; i++) {
        numbers.push({
            letter: i.toString(),
            word: i.toString(),
            emoji: '🔢',
            color: i % 2 === 0 ? 'bg-blue-50' : 'bg-green-50',
            phonics: `Number ${i}. Can you count to ${i}?`
        });
    }
    return numbers;
};

const getTablesData = () => {
    const tables = [];
    for (let i = 1; i <= 12; i++) {
        const tableLines = [];
        const spokenLines = [`Let's learn the table of ${i}. Listen carefully.`];

        for (let j = 1; j <= 10; j++) {
            tableLines.push(`${i} x ${j} = ${i * j}`);
            spokenLines.push(`${i} times ${j} is ${i * j}.`);
        }

        tables.push({
            number: `Table of ${i}`,
            equation: tableLines.join('\n'),
            emoji: '✖️',
            color: 'bg-indigo-50',
            phonics: spokenLines.join(' ') + " Great job!"
        });
    }
    return tables;
};

export const NUMBERS_DATA = getNumbersData();
export const TABLES_DATA = getTablesData();
